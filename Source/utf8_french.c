int split_add_translate_data_line(char *line);

int use_default_translate_data() {
	int i = 0;
	const char *s;
	char buf[2048 + 1];

	while ((s = default_translate_data[i]) != NULL) {
		// Copy const string into buffer for the split
		strncpy(buf, s, 2048);
		buf[2048] = '\0';
		split_add_translate_data_line(buf);
		i++;
	}

	return i;
}